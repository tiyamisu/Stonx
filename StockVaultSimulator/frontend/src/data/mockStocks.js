// Helper: generate a realistic 30-day price history from a base price
function generatePriceHistory(basePrice, volatility = 0.015) {
  const history = [];
  let price = basePrice * (0.85 + Math.random() * 0.1); // start ~15% lower
  for (let i = 30; i >= 0; i--) {
    const change = (Math.random() - 0.48) * volatility * price;
    price = Math.max(price + change, 1);
    const date = new Date();
    date.setDate(date.getDate() - i);
    history.push({
      date: date.toLocaleDateString("en-US", { month: "short", day: "numeric" }),
      price: parseFloat(price.toFixed(2)),
    });
  }
  return history;
}

